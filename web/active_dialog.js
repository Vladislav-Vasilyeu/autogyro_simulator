function getAnswer(question) {
    var prepared = normalizeText(question);

    if (!prepared) {
        return "Введите вопрос.";
    }

    var questionTokens = getTokens(prepared);
    var questionStems = questionTokens.map(stemWord);
    var questionAttribute = getQuestionAttribute(prepared);
    var asksAutogyroModel = hasAny(prepared, ["м 24", "м-24", "орион", "автожир"]);
    var asksPrincipleVideo = asksPrinciple(prepared);
    var bestTriad = null;
    var bestScore = 0;

    for (var i = 0; i < knowleage.length; i++) {
        var triad = knowleage[i];
        var subject = normalizeText(triad[0]);
        var predicate = normalizeText(triad[1]);
        var object = normalizeText(triad[2]);
        var full = subject + " " + predicate + " " + object;
        var score = 0;

        score += countMatches(questionStems, subject) * 5;
        score += countMatches(questionStems, predicate) * 7;
        score += countMatches(questionStems, object) * 4;
        score += countMatches(questionStems, full);

        if (questionTokens.indexOf(predicate) !== -1) {
            score += 22;
        }

        if (hasPredicateMatch(questionTokens, predicate)) {
            score += 8;
        }

        if (subject && prepared.indexOf(subject) !== -1) {
            score += 16;
        }

        if (object && object.length > 6 && prepared.indexOf(object) !== -1) {
            score += 8;
        }

        if (prepared.indexOf("кто") !== -1 && /^[А-ЯЁA-Z]/.test(triad[0])) {
            score += 4;
        }

        if (prepared.indexOf("что такое") !== -1 && subject && prepared.indexOf(subject) !== -1) {
            if (/^(является|представляет|состоит)$/.test(predicate)) {
                score += 24;
            }
        }

        if ((prepared.indexOf("какие") !== -1 || prepared.indexOf("какой") !== -1 || prepared.indexOf("бывают") !== -1 || prepared.indexOf("типы") !== -1) &&
            /(бывают|различает|делятся|разделяются|включает)/.test(predicate + " " + object)) {
            score += 18;
        }

        if (prepared.indexOf("какая") !== -1 || prepared.indexOf("какой") !== -1 || prepared.indexOf("сколько") !== -1) {
            if (/\d|метр|километр|масса|скорость|длина|высота|диаметр|потолок|дальность/.test(object)) {
                score += 6;
            }
        }

        if (questionAttribute && (prepared.indexOf("сколько") !== -1 || prepared.indexOf("какая") !== -1 || prepared.indexOf("какой") !== -1 || prepared.indexOf("чему") !== -1)) {
            if (textMatchesAttribute(object, questionAttribute) || textMatchesAttribute(subject, questionAttribute)) {
                score += 45;
            } else if (/\d/.test(object)) {
                score -= 10;
            } else {
                score -= 18;
            }
        }

        if (questionAttribute === "масса" && /(масса|килограмм|кг)/.test(object)) {
            score += 60;
        }

        if (asksAutogyroModel && subject.indexOf("м-24") !== -1) {
            score += 10;
        }

        if (asksPrincipleVideo && /<video\s/i.test(object)) {
            score += 100;
        }

        if (score > bestScore) {
            bestScore = score;
            bestTriad = triad;
        }
    }

    return bestScore >= 5 ? addAutogyroPhoto(triadToAnswer(bestTriad)) : "Ответ не найден.";
}

