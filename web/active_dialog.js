function dialog_window() {
    if (document.getElementById("floatingDialog")) {
        return;
    }

    var dialog = document.createElement("div");
    dialog.id = "floatingDialog";
    dialog.className = "floating-dialog";
    dialog.innerHTML =
        "<button class='dialog-label' type='button' onclick='openDialog()'>Спросить</button>" +
        "<div class='dialog-shell'>" +
            "<div class='dialog-header'>" +
                "<strong>Диалог с базой знаний</strong>" +
                "<button type='button' onclick='openDialog()' aria-label='Закрыть'>×</button>" +
            "</div>" +
            "<div class='dialog-history' id='dialogHistory'></div>" +
            "<form class='dialog-form' id='dialogForm'>" +
                "<input id='Qdialog' type='text' placeholder='Введите вопрос' autocomplete='off'>" +
                "<button type='button' onclick='ask()'>Спросить</button>" +
            "</form>" +
        "</div>";

    document.body.appendChild(dialog);

    document.getElementById("dialogForm").addEventListener("submit", function(e) {
        e.preventDefault();
        ask();
    });

    addDialogMessage("answer", "Здравствуйте. Задайте вопрос об автожире, М-24 «Орион», авторотации или элементах установки.");
    initVoiceInput();
}

function openDialog() {
    var dialog = document.getElementById("floatingDialog");

    if (!dialog) {
        return;
    }

    dialogOn = !dialogOn;
    dialog.classList.toggle("open", dialogOn);

    if (dialogOn) {
        var input = document.getElementById("Qdialog");
        if (input) {
            input.focus();
        }
    }
}

function ask(inputId) {
    var input = document.getElementById(inputId || "Qdialog");
    var question = input ? input.value.trim() : "";

    if (!question) {
        return;
    }

    if (!dialogOn) {
        openDialog();
    }

    addDialogMessage("question", question);

    var answer = typeof getAnswer === "function" ? getAnswer(question) : "Модуль базы знаний не загружен.";
    addDialogMessage("answer", answer);
    speakDialogAnswer(answer);

    if (input) {
        input.value = "";
        input.focus();
    }
}

function addDialogMessage(type, text) {
    var history = document.getElementById("dialogHistory");
    var message = document.createElement("div");

    if (!history) {
        return;
    }

    message.className = "dialog-message " + type;
    message.innerHTML = text;
    history.appendChild(message);
    history.scrollTop = history.scrollHeight;
}

function getPlainAnswerText(text) {
    var div = document.createElement("div");
    div.innerHTML = String(text || "");

    return (div.textContent || div.innerText || "")
        .replace(/\s+/g, " ")
        .trim();
}

function speakDialogAnswer(text) {
    var plainText = getPlainAnswerText(text);

    if (!plainText) {
        return;
    }

    if (window.speechSynthesis && window.SpeechSynthesisUtterance) {
        window.speechSynthesis.cancel();

        var utterance = new SpeechSynthesisUtterance(plainText);
        utterance.lang = "ru-RU";
        utterance.rate = 1;
        window.speechSynthesis.speak(utterance);
    }
}

function initVoiceInput() {
    var input = document.getElementById("Qdialog");

    if (!input) {
        return;
    }

    var voiceButton = document.createElement("button");
    voiceButton.type = "button";
    voiceButton.className = "dialog-voice";
    voiceButton.title = "Ввести вопрос голосом через Web Speech API";
    voiceButton.textContent = "🎙";

    input.insertAdjacentElement("afterend", voiceButton);

    voiceButton.addEventListener("click", function() {
        startVoiceInput();
    });
}

function startVoiceInput() {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
        addDialogMessage("answer", "Голосовой ввод не поддерживается браузером. Используйте Chrome, Edge или другой современный браузер.");
        return;
    }

    var SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    var recognition = new SpeechRecognition();

    recognition.lang = "ru-RU";
    recognition.interimResults = false;
    recognition.maxAlternatives = 1;

    var voiceButton = document.querySelector(".dialog-voice");
    if (voiceButton) {
        voiceButton.classList.add("listening");
        voiceButton.disabled = true;
        voiceButton.textContent = "■";
        addDialogMessage("answer", "Говорите...");
    }

    recognition.start();

    recognition.onresult = function(event) {
        var text = event.results[0][0].transcript;
        var input = document.getElementById("Qdialog");

        if (input) {
            input.value = text;
            input.focus();
            ask("Qdialog");
        }

        if (voiceButton) {
            voiceButton.classList.remove("listening");
            voiceButton.disabled = false;
            voiceButton.textContent = "🎙";
        }
    };

    recognition.onerror = function(event) {
        addDialogMessage("answer", "Ошибка распознавания: " + event.error);

        if (voiceButton) {
            voiceButton.classList.remove("listening");
            voiceButton.disabled = false;
            voiceButton.textContent = "🎙";
        }
    };

    recognition.onend = function() {
        if (voiceButton) {
            voiceButton.classList.remove("listening");
            voiceButton.disabled = false;
            voiceButton.textContent = "🎙";
        }
    };
}

