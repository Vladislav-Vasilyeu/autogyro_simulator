function stemWord(word) {
    var result = normalizeText(word);
    var endingsToCut = [
        "иями", "ями", "ами", "ого", "ему", "ому", "ими", "ыми", "ией", "иям",
        "иях", "ать", "ять", "ить", "ется", "ются", "ится", "атся", "ятся",
        "ого", "его", "ому", "ему", "ыми", "ими", "ая", "яя", "ое", "ее",
        "ые", "ие", "ый", "ий", "ой", "ых", "их", "ую", "юю", "ам", "ям",
        "ах", "ях", "ов", "ев", "ей", "ою", "ею", "ом", "ем", "ым", "им",
        "ал", "ил", "ел", "ла", "ло", "ли", "ся", "сь", "а", "я", "ы",
        "и", "е", "у", "ю", "о"
    ];

    for (var i = 0; i < endingsToCut.length; i++) {
        var ending = endingsToCut[i];
        if (result.length > ending.length + 3 && result.endsWith(ending)) {
            return result.slice(0, -ending.length);
        }
    }

    return result;
}

function getTokens(text) {
    return normalizeText(text)
        .split(" ")
        .map(function(word) {
            return word.trim();
        })
        .filter(function(word) {
            return word.length > 1 && serviceWords.indexOf(word) === -1;
        });
}

function getStems(text) {
    return getTokens(text).map(stemWord).filter(function(word) {
        return word.length > 1;
    });
}

function countMatches(questionStems, text) {
    var stems = getStems(text);
    var used = {};
    var count = 0;

    for (var i = 0; i < questionStems.length; i++) {
        for (var j = 0; j < stems.length; j++) {
            if (used[j]) {
                continue;
            }

            if (questionStems[i] === stems[j] ||
                (questionStems[i].length > 4 && stems[j].indexOf(questionStems[i]) === 0) ||
                (stems[j].length > 4 && questionStems[i].indexOf(stems[j]) === 0)) {
                used[j] = true;
                count++;
                break;
            }
        }
    }

    return count;
}

function hasAny(text, words) {
    for (var i = 0; i < words.length; i++) {
        if (text.indexOf(words[i]) !== -1) {
            return true;
        }
    }

    return false;
}

function getQuestionAttribute(prepared) {
    var attributes = [
        ["масса", ["масса", "масс", "вес", "весит", "тяжел"]],
        ["скорость", ["скорость", "скорост"]],
        ["длина", ["длина", "длину", "длин"]],
        ["высота", ["высота", "высоту", "высот"]],
        ["диаметр", ["диаметр", "винта", "ротора"]],
        ["потолок", ["потолок", "высота полета"]],
        ["дальность", ["дальность", "далеко", "расстояние"]],
        ["мощность", ["мощность", "лошадиных сил"]]
    ];

    for (var i = 0; i < attributes.length; i++) {
        if (hasAny(prepared, attributes[i][1])) {
            return attributes[i][0];
        }
    }

    return "";
}

function textMatchesAttribute(text, attribute) {
    var patterns = {
        "масса": /(масса|масс|вес|килограмм|кг)/,
        "скорость": /(скорость|скорост|километр[а-я ]*час)/,
        "длина": /(длина|длину|длин[а-я]*|\d+[,.]?\d*\s*метр)/,
        "высота": /(высота|высоту|высот[а-я]*|\d+[,.]?\d*\s*метр)/,
        "диаметр": /(диаметр|винт|ротор)/,
        "потолок": /(потолок|высота полета)/,
        "дальность": /(дальность|дальност|километр)/,
        "мощность": /(мощность|лошадин[а-я]* сил)/
    };

    return patterns[attribute] ? patterns[attribute].test(text) : false;
}

function hasPredicateMatch(questionTokens, predicateText) {
    var normalizedPredicate = normalizeText(predicateText);

    for (var i = 0; i < questionTokens.length; i++) {
        if (normalizeText(questionTokens[i]) === normalizedPredicate) {
            return true;
        }

        if (getPredicateRegExp(questionTokens[i], questionTokens[i + 1]).test(normalizedPredicate)) {
            return true;
        }
    }

    return false;
}

function getPredicateRegExp(word, nextWord) {
    var normalized = normalizeText(word);

    for (var i = 0; i < endings.length; i++) {
        var ending = endings[i][0];
        var replacement = endings[i][1];

        if (normalized.endsWith(ending) && normalized.length > ending.length + 1) {
            var basis = escapeRegExp(normalized.slice(0, -ending.length));
            return new RegExp(basis + replacement, "i");
        }
    }

    if (nextWord) {
        return new RegExp(escapeRegExp(normalized + " " + normalizeText(nextWord)), "i");
    }

    return new RegExp(escapeRegExp(normalized), "i");
}

