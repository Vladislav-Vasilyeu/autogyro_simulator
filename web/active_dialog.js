function small1(str) {
    str = String(str || "");
    return str.charAt(0).toLowerCase() + str.slice(1);
}

function big1(str) {
    str = String(str || "");
    return str.charAt(0).toUpperCase() + str.slice(1);
}

function escapeRegExp(str) {
    return String(str).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function normalizeText(str) {
    return small1(str)
        .replace(/<[^>]*>/g, " ")
        .replace(/ё/g, "е")
        .replace(/["'«»]/g, " ")
        .replace(/[?!.:,;()]/g, " ")
        .replace(/\s+/g, " ")
        .trim();
}

